    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String puesto = (String) ComboPues.getSelectedItem();
        String cargo = null;
        String sql = "SELECT idpuesto FROM puesto WHERE nombre_puesto= ? ;";
        try {
            PreparedStatement declaracion = conexionCuentas.getConexion().prepareStatement(sql);
            declaracion.setString(1, puesto);

            //declaracion.execute();
            ResultSet result1 = declaracion.executeQuery();
            while (result1.next()) {
                cargo = result1.getString("idpuesto");
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        CostosPlanilla planilla = new CostosPlanilla();
        String dui = tdui.getText();
        String nombre = tnombre.getText();
        String apellidos = tapellido.getText();
        if (dui.isEmpty() && nombre.isEmpty() && apellidos.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No hay parametros para ingresar Empleado");
        } else {
            planilla.guardarEmpleado(dui, nombre, apellidos, tfecha.getDate().getTime(), cargo);
            JOptionPane.showMessageDialog(this, "Empleado Guardado");
            tdui.setText(null);
            tnombre.setText(null);
            tapellido.setText(null);
            tfecha.setDate(null);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

