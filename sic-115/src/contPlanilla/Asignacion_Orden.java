private void FillcomboOrden() {
        try {
            comboOrden.removeAllItems();
            String sqlGeneral = "select * from orden";
            PreparedStatement declaracion1;
            declaracion1 = null;
            declaracion1 = this.conexionCuentas.getConexion().prepareCall(sqlGeneral);
            ResultSet resultado1 = declaracion1.executeQuery();
            while (resultado1.next()) {
                String name = resultado1.getString("id_orden");
                comboOrden.addItem(name);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void FillcomboEmpleado() {
        try {
            comboEmpleado.removeAllItems();
            String sqlGeneral = "select * from empleado";
            PreparedStatement declaracion1;
            declaracion1 = null;
            declaracion1 = this.conexionCuentas.getConexion().prepareCall(sqlGeneral);
            ResultSet resultado1 = declaracion1.executeQuery();
            while (resultado1.next()) {
                String name = resultado1.getString("id_empleado");
                comboEmpleado.addItem(name);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void inicializarColumnas() {
        TableColumnModel tColumnModel = new DefaultTableColumnModel();
        for (int i = 0; i < 5; i++) {
            TableColumn col = new TableColumn(i);
            switch (i) {
                case 0:
                    col.setHeaderValue("DUI");
                    break;
                case 1:
                    col.setHeaderValue("Nombre");
                    break;
                case 2:
                    col.setHeaderValue("Puesto");
                    break;
                case 3:
                    col.setHeaderValue("Orden");
                    break;
                case 4:
                    col.setHeaderValue("Descripci贸n");

            }
            tColumnModel.addColumn(col);
        }
        this.tablaEmpleados.setColumnModel(tColumnModel);

    }

    public void refrescarTabla() {

        this.list.clear();
        this.listaC.generarLista();
        list = listaC.getListaEmpleados();

        for (Empleado c : this.list) {
            this.modelEmpleado.empleados.add(c);

        }
        modelEmpleado.fireTableDataChanged();
    }
