    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Double por = Double.parseDouble(txtcostos.getText().toString());
        String sql = " UPDATE cif SET porcentaje = ?;";

        try {
            PreparedStatement declaracion = conexionCuentas.getConexion().prepareStatement(sql);
            declaracion.setDouble(1, por);
            declaracion.execute();
            JOptionPane.showMessageDialog(this, "Ha cambiado exitosamente los Costos Indirectos de Fabricacion");
        } catch (SQLException e) {
            e.printStackTrace();
        }
        txtcostos.setText(null);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void txtcostosKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcostosKeyTyped
        int k = (int) evt.getKeyChar();
        //Primer if no permite el ingreso de letras y otros símbolos
        if ((k >= 32 && k <= 45) || (k >= 58 && k <= 126)) {
            evt.setKeyChar((char) KeyEvent.VK_CLEAR);
            evt.consume();
        }
        //Segundo if no permite el ingreso de "ñ" ," Ñ" ni "/"
        if (k == 241 || k == 209 || k == 47) {
            evt.setKeyChar((char) KeyEvent.VK_CLEAR);
            evt.consume();
        }
    }//GEN-LAST:event_txtcostosKeyTyped

