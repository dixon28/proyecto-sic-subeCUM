    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (!(libroactual.getFechainicio().after(this.tcreacion.getDate()) && libroactual.getFechafin().before(this.tcreacion.getDate()))) {

            String puesto = (String) comboPan.getSelectedItem();
            String pan = null;
            String sql = "SELECT id_pan FROM pan WHERE nombre_pan= ? ;";
            try {
                PreparedStatement declaracion = conexionCuentas.getConexion().prepareStatement(sql);
                declaracion.setString(1, puesto);

                //declaracion.execute();
                ResultSet result1 = declaracion.executeQuery();
                while (result1.next()) {
                    pan = result1.getString("id_pan");
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }

            CostosPlanilla planilla = new CostosPlanilla();
            String numeroOrden = torden.getText();
            String sueldo = tSueldo.getText();
            String numero = tnumero.getText();
            String descripcion = tdescripcion.getText();
            if (sueldo.isEmpty() && numero.isEmpty() && descripcion.isEmpty()) {
                JOptionPane.showMessageDialog(null, "No hay parametros para ingresar Orden");
            } else {
                planilla.guardarOrden(numeroOrden, pan, sueldo, numero, descripcion, tcreacion.getDate().getTime(), tentrega.getDate().getTime());
                JOptionPane.showMessageDialog(this, "Orden Guardada");
                tSueldo.setText(null);
                tnumero.setText(null);
                tdescripcion.setText(null);
                torden.setText(null);
                tcreacion.setDate(null);
                tentrega.setDate(null);
            }

        } else {
            JOptionPane.showMessageDialog(this, "La fecha no esta dentro del periodo contable, ingrese fecha correcta");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

