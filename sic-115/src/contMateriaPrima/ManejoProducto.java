private void inicializarColumnas() {
        TableColumnModel tColumnModel = new DefaultTableColumnModel();
        for (int i = 0; i < 5; i++) {
            TableColumn col = new TableColumn(i);
            switch (i) {
                case 0:
                    col.setHeaderValue("Numero de Orden");
                    break;
                case 1:
                    col.setHeaderValue("Tipo de Pan");
                    break;
                case 2:
                    col.setHeaderValue("Costos de Materia Prima");
                    break;
                case 3:
                    col.setHeaderValue("Costos Indirectos de Fabricacion");
                    break;
                case 4:
                    col.setHeaderValue("Costos de Mano de Obra");

            }
            tColumnModel.addColumn(col);
        }
        this.tablaOrden.setColumnModel(tColumnModel);

    }

    private void cargarTabla() {
        modelo.ordenes.clear();
        for (Orden o : list.getListaOrden()) {

            this.modelo.ordenes.add(o);
        }

        this.modelo.fireTableDataChanged();
    }
