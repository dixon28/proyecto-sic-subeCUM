    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int valor = 0;
        if (admin.isSelected()) {
            valor = 1;
        } else {
            valor = 2;
        }
        cierreOrden cierre = new cierreOrden();
        if (nombre.getText().isEmpty() || contra.getText().isEmpty() || usuario.getText().isEmpty() || !((admin.isSelected()) || normal.isSelected())) {
            JOptionPane.showMessageDialog(null, "no hay parametros o faltan para ingresar Usuario");
        } else {
            cierre.RegistrarUsuario(nombre.getText().toString(), contra.getText().toString(), usuario.getText().toString(), valor);
 
            nombre.setText(null);
            contra.setText(null);
            usuario.setText(null);
            buttonGroup1.clearSelection();

        }
    }//GEN-LAST:event_jButton1ActionPerformed

