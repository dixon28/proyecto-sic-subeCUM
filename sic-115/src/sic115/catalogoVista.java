    private void inicializarColumnas() {
        TableColumnModel tColumnModel = new DefaultTableColumnModel();
        for (int i = 0; i < 3; i++) {
            TableColumn col = new TableColumn(i);
            switch (i) {
                case 0:
                    col.setHeaderValue("Código");
                    break;
                case 1:
                    col.setHeaderValue("Nombre");
                    break;
                case 2:
                    col.setHeaderValue("Descripción");

            }
            tColumnModel.addColumn(col);
        }
        this.tablaCuentas.setColumnModel(tColumnModel);

    }

    public void autocompletado() {

        TextAutoCompleter textComple = new TextAutoCompleter(this.txtBuscar);
        textComple.removeAll();
        listaC.generarLista();
        list = listaC.getCuentasOrdenadas();
        for (Cuenta c : this.list) {
            textComple.addItem(c.getNombre());

        }
    }

    public void refrescarTabla() {
        list.clear();
        this.listaC.generarLista();
        list = listaC.getCuentasOrdenadas();
        this.modelCatalogo.cuentas.clear();
        for (Cuenta c : this.list) {
            this.modelCatalogo.cuentas.add(c);

        }
        modelCatalogo.fireTableDataChanged();
    }
