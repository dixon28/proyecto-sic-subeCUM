    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

OpCierreMP op= new OpCierreMP();
op.recuperarMPanteriorAkardex(activo.getActivoActual().getFechainicio());
        this.modelCatalogo.cuentas.clear();
        boolean x = false;
        String buscar = this.txtBuscar.getText().toString();
        list.clear();
        listaC.generarLista();

        list = listaC.getCuentasOrdenadas();

        if (buscar.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No hay parametros que buscar");

        } else {

            for (Cuenta c : this.list) {

                if (c.getNombre().equals(buscar)) {
                    this.modelCatalogo.cuentas.add(c);
                    x = true;

                }
            }

            if (x) {
            
                this.modelCatalogo.fireTableDataChanged();

            } else {
                JOptionPane.showMessageDialog(null, "No hay elemento especificado");
            }

        }

    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        CuentaConsultas cuent = new CuentaConsultas();
        String codigo = this.tcodigo.getText().toString();
        String nombre = this.tnombre.getText().toString();
        String descripcion = this.tdescripcion.getText().toString();
        String tipo1 = tipo;
        if (nombre.isEmpty() || !((Cuenta.isSelected()) || Subcuenta.isSelected())) {

            JOptionPane.showMessageDialog(null, "no hay parametros o faltan para editar la cuenta");

        } else {
            cuent.modificar(codigo, nombre, descripcion, tipo);
            JOptionPane.showMessageDialog(this, "Cuenta Modificada");
            tcodigo.setText(null);
            tnombre.setText(null);
            tdescripcion.setText(null);
            buttonGroup2.clearSelection();
        }

        refrescarTabla();
    }//GEN-LAST:event_btnModificarActionPerformed

