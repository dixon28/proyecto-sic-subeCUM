    private void llenarCombo() {

        DefaultComboBoxModel value;
        this.comboActivos.removeAllItems();

        comboActivos.removeAllItems();
        value = new DefaultComboBoxModel();
        comboActivos.setModel(value);
        this.ListaLibros.clear();
        ListaLibros = list.getActivosEinactivos();
        for (libroDiario aux : ListaLibros) {
            libroDiario a = aux;
            value.addElement(a);

        }
        this.comboActivos.repaint();
    }

