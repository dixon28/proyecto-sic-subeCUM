    private void CerrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CerrarActionPerformed
        int aprobacion;
        aprobacion = 1;
        libroDiario libroAfectado = (libroDiario) this.comboActivos.getSelectedItem();
        if (libroAfectado.getCerrado()) {
            JOptionPane.showMessageDialog(this, "Este periodo ya esta cerrado");
        } else {
            aprobacion = JOptionPane.showConfirmDialog(this, "Una vez cerrado, el periodo contable no se podra abrir Â¿Esta seguro?");

        }
        if (aprobacion == 0) {
            try {

                cierre.crearcopia(libroAfectado.getId());
                productoT.baguette();
                productoT.galletaMaria();
                productoT.productoTerminadoVacio();
                cierremp.copiaKardex();
                cierremp.recuperarMPAzucar();
                cierremp.recuperarMPHarina();
                cierremp.recuperarMPHuevos();
                cierremp.recuperarMPLevadura();
                cierremp.recuperarMantecaMP();
                cierremp.recuperarMpsal();
                
                
                cierremp.vaciarkardex();
                

                this.list.cerrarLibro();

                JOptionPane.showMessageDialog(this, "Periodo cerrado con exito");
                this.llenarCombo();
                this.comboActivos.repaint();

            } catch (SQLException ex) {
                Logger.getLogger(finalIniPeriodo.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_CerrarActionPerformed

